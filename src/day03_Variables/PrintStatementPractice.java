package day03_Variables;

public class PrintStatementPractice {
    public static void main(String[] args) {

        System.out.print("Hello"); // does not append new line
        System.out.print("Hola"); // does not append new line

        System.out.println("How are you?"); // appends new line after printing

        System.out.print("Today is Friday"); // does not append new line
        System.out.println("Tomorrow is Saturday");

        System.out.println("--------------------------------------------");
        System.out.println("\tHello Batch 23\n\tHow are you today?");
        System.out.println("My name is \"Muhtar\"");
        System.out.println("My name is 'Robin'");

    }
}
